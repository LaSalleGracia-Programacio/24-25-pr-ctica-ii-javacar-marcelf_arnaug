package org.JavaCar;

public class Usuari {
    private String nom;
    private String cognom;
    private String telefon;
    private String email;

    public Usuari(String nom, String cognom, String telefon, String email) {
        this.nom = nom;
        this.cognom = cognom;
        this.telefon = telefon;
        this.email = email;
    }
}
